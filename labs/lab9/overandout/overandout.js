let dvBox = document.getElementById("dvBox");

dvBox.style.width = "100px";
dvBox.style.height = "100px";
dvBox.style.backgroundColor = "#2881ed";

function mouseOver() {
    dvBox.style.width = "100px";
    dvBox.style.height = "100px";
    dvBox.style.backgroundColor = "#000000";
}

function mouseOut() {
    dvBox.style.width = "100px";
    dvBox.style.height = "100px";
    dvBox.style.backgroundColor = "#2881ed";
}